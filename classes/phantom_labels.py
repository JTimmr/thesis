def build_delta_grid(
    tick_size: float, max_delta: float, delta_lo: float,
) -> np.ndarray:
    """Return the half-tick support possible across all legal BBO midpoints."""
    step = float(tick_size)
    lo = float(delta_lo)
    hi = float(max_delta)
    if not math.isfinite(step) or step <= 0.0:
        raise ValueError("tick_size must be positive")
    if not (math.isfinite(lo) and math.isfinite(hi)) or hi < lo:
        raise ValueError("max_delta must be finite and >= delta_lo")
    index_lo = int(math.ceil(lo / step - 1e-9))
    index_hi = int(math.floor(hi / step + 1e-9))
    if index_hi < index_lo:
        return np.empty(0, dtype=np.float64)
    return step * np.arange(index_lo, index_hi + 1, dtype=np.float64)


INVALID_PRICE_TICK = np.iinfo(np.int64).min


def _price_to_tick_index(price: float, tick_size: float) -> int:
    """Convert an executable price to its integer tick index."""
    price_value = float(price)
    tick = float(tick_size)
    if not math.isfinite(tick) or tick <= 0.0:
        raise ValueError("tick_size must be positive")
    if not math.isfinite(price_value):
        raise ValueError(f"price must be finite, got {price!r}")
    scaled = price_value / tick
    nearest = int(round(scaled))
    if not math.isclose(
        scaled, nearest, rel_tol=0.0, abs_tol=1e-6,
    ):
        raise ValueError(
            f"price {price!r} is not aligned to tick_size={tick_size!r}"
        )
    return nearest


def _prices_to_tick_indices(
    prices: np.ndarray, tick_size: float,
) -> np.ndarray:
    """Vectorised price-to-tick conversion; non-finite prices use a sentinel."""
    tick = float(tick_size)
    if not math.isfinite(tick) or tick <= 0.0:
        raise ValueError("tick_size must be positive")
    values = np.asarray(prices, dtype=np.float64)
    out = np.full(values.shape, INVALID_PRICE_TICK, dtype=np.int64)
    finite = np.isfinite(values)
    if not finite.any():
        return out
    scaled = values[finite] / tick
    nearest = np.rint(scaled)
    aligned = np.isclose(scaled, nearest, rtol=0.0, atol=1e-6)
    if not aligned.all():
        bad = values[finite][~aligned][:5]
        raise ValueError(
            f"found off-tick order prices for tick_size={tick_size}: "
            f"{bad.tolist()}"
        )
    out[finite] = nearest.astype(np.int64)
    return out


def build_legal_tick_state(
    best_bid: float,
    best_ask: float,
    bid_depth: np.ndarray,
    ask_depth: np.ndarray,
    side: int,
    *,
    tick_size: float,
    delta_lo: float,
    max_delta: float,
) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Return legal phantom prices, mid-relative deltas, and queue ahead.

    Candidate prices are integer market ticks and cannot cross the opposite
    BBO.  Delta is represented internally in half-ticks because the midpoint
    of two legal prices can lie halfway between ticks.
    """
    tick = float(tick_size)
    if not math.isfinite(tick) or tick <= 0.0:
        raise ValueError("tick_size must be positive")
    if float(max_delta) < float(delta_lo):
        raise ValueError("max_delta must be >= delta_lo")
    if side not in (1, 2):
        raise ValueError(f"side must be 1 (bid) or 2 (ask), got {side!r}")

    bid_tick = _price_to_tick_index(best_bid, tick)
    ask_tick = _price_to_tick_index(best_ask, tick)
    if ask_tick <= bid_tick:
        raise ValueError(
            f"invalid BBO: best_bid={best_bid!r}, best_ask={best_ask!r}"
        )

    midpoint_2ticks = bid_tick + ask_tick
    half_tick = tick / 2.0
    delta_2ticks_lo = int(math.ceil(float(delta_lo) / half_tick - 1e-9))
    delta_2ticks_hi = int(math.floor(float(max_delta) / half_tick + 1e-9))

    def ceil_div2(value: int) -> int:
        return -((-value) // 2)

    if side == 1:
        price_tick_lo = max(
            1, ceil_div2(midpoint_2ticks - delta_2ticks_hi),
        )
        price_tick_hi = min(
            ask_tick - 1,
            (midpoint_2ticks - delta_2ticks_lo) // 2,
        )
    else:
        price_tick_lo = max(
            1,
            bid_tick + 1,
            ceil_div2(midpoint_2ticks + delta_2ticks_lo),
        )
        price_tick_hi = (midpoint_2ticks + delta_2ticks_hi) // 2

    if price_tick_hi < price_tick_lo:
        empty = np.empty(0, dtype=np.float64)
        return empty, empty.copy(), empty.copy()

    if side == 1:
        price_ticks = np.arange(
            price_tick_hi, price_tick_lo - 1, -1, dtype=np.int64,
        )
        delta_2ticks = midpoint_2ticks - 2 * price_ticks
        cumulative_depth = np.concatenate(
            ([0.0], np.cumsum(np.asarray(bid_depth, dtype=np.float64))),
        )
        depth_level = bid_tick - price_ticks
    else:
        price_ticks = np.arange(
            price_tick_lo, price_tick_hi + 1, dtype=np.int64,
        )
        delta_2ticks = 2 * price_ticks - midpoint_2ticks
        cumulative_depth = np.concatenate(
            ([0.0], np.cumsum(np.asarray(ask_depth, dtype=np.float64))),
        )
        depth_level = price_ticks - ask_tick

    queue_index = np.clip(
        depth_level + 1, 0, cumulative_depth.shape[0] - 1,
    )
    prices = price_ticks.astype(np.float64) * tick
    deltas = delta_2ticks.astype(np.float64) * half_tick
    queue_ahead = cumulative_depth[queue_index]
    return prices, deltas, queue_ahead


def build_feature_names(n_kernels: int, n_levels: int) -> List[str]:
    """Named columns for the per-``t`` feature vector, in stable order.

    For ``n_kernels == 1``, only the six total Hawkes intensities are kept;
    per-kernel rows would duplicate ``λ = μ +`` that single excitation row
    (up to clipping).  For ``n_kernels > 1``, ``hawkes_kernel{k}_*`` columns
    are included so time-scale decomposition is preserved.
    """
    names: List[str] = []
    # Hawkes intensity per dimension (6)
    for lbl in DEFAULT_LABELS:
        names.append(f"hawkes_intensity_{lbl}")
    # Per-kernel decomposition (n_kernels * 6); omitted when K == 1
    if n_kernels > 1:
        for k in range(n_kernels):
            for lbl in DEFAULT_LABELS:
                names.append(f"hawkes_kernel{k}_{lbl}")
    # Book depth (2 * n_levels)
    for i in range(n_levels):
        names.append(f"bid_depth_L{i}")
    for i in range(n_levels):
        names.append(f"ask_depth_L{i}")
    # Scalar features
    names.extend(["spread_ticks", "l0_imbalance", "recent_vol", "tod_frac"])
    return names


# Default book depth levels used as NN features (L0 .. L{n-1}).
PHANTOM_DEFAULT_N_BOOK_LEVELS = 10
# Default depth levels used for queue_ahead computation in labelling.
PHANTOM_DEFAULT_N_QUEUE_LEVELS = 40
# Export tuple for docs / smoke tests: single kernel + default depth count
# (no hawkes_kernel* columns when K == 1).
PHANTOM_PER_T_FEATURES: Tuple[str, ...] = tuple(
    build_feature_names(1, PHANTOM_DEFAULT_N_BOOK_LEVELS)
)


# --- Volatility feature ---


def realized_vol_time_grid(
    t_sec: np.ndarray,
    mid: np.ndarray,
    window_s: float = 200.0,
    sample_dt_s: float = 1.0,
    floor: float = 1e-8,
) -> np.ndarray:
    """Cadence-independent trailing realised volatility, aligned to inputs.

    The mid series is resampled onto a uniform ``sample_dt_s`` grid spanning
    ``[t_sec[0], t_sec[-1]]`` via last-observation-carried-forward.  Realised
    volatility at a grid point is the RMS of the log-mid returns over the
    trailing ``window_s`` seconds (i.e. the last ``round(window_s/sample_dt_s)``
    grid returns).  Each input row ``i`` is then assigned the realised vol of
    the grid cell containing ``t_sec[i]``, so the result is aligned 1:1 with the
    input arrays and is independent of the event cadence of ``t_sec``/``mid``.

    Parameters
    ----------
    t_sec : np.ndarray
        Per-row timestamps in seconds (monotonic non-decreasing).
    mid : np.ndarray
        Per-row mid prices (same length as ``t_sec``).
    window_s : float
        Trailing window length in seconds (default 200).
    sample_dt_s : float
        Grid spacing in seconds (default 1).
    floor : float
        Variance floor (the returned vol is ``>= sqrt(floor)``).

    Returns
    -------
    np.ndarray
        Realised vol per input row, shape ``(len(t_sec),)``.
    """
    n = int(t_sec.shape[0])
    out = np.full(n, math.sqrt(floor), dtype=np.float64)
    if n == 0:
        return out
    dt = float(sample_dt_s)
    if dt <= 0:
        raise ValueError("sample_dt_s must be positive")
    t0 = float(t_sec[0])
    span = float(t_sec[-1]) - t0
    if not np.isfinite(span) or span < dt:
        return out  # too short for a single grid return -> floor everywhere

    n_win = max(1, int(round(float(window_s) / dt)))
    n_grid = int(math.floor(span / dt))  # grid points 1..n_grid (0 is t0)
    grid_t = t0 + dt * np.arange(n_grid + 1, dtype=np.float64)

    # LOCF resample: mid at the last input row with t_sec <= grid time.
    pos = np.searchsorted(t_sec, grid_t, side="right") - 1
    np.clip(pos, 0, n - 1, out=pos)
    grid_mid = np.asarray(mid, dtype=np.float64)[pos]

    valid = (grid_mid > 0) & np.isfinite(grid_mid)
    with np.errstate(divide="ignore", invalid="ignore"):
        ret = np.log(grid_mid[1:] / grid_mid[:-1])
    ret = np.where(valid[1:] & valid[:-1], ret, 0.0)
    r2 = ret * ret

    # Trailing-window mean of squared returns via cumulative sums.
    csum = np.concatenate(([0.0], np.cumsum(r2)))  # len n_grid + 1
    k = np.arange(n_grid + 1)
    lo = np.maximum(0, k - n_win)
    cnt = np.maximum(k - lo, 1)
    meansq = (csum[k] - csum[lo]) / cnt
    var = np.maximum(meansq, floor)
    var[0] = floor
    vol_grid = np.sqrt(var)

    # Map each input row to its grid cell.
    cell = np.floor((t_sec.astype(np.float64) - t0) / dt).astype(np.int64)
    np.clip(cell, 0, n_grid, out=cell)
    return vol_grid[cell]


